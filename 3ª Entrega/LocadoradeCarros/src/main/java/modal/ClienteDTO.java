package modal;

public class ClienteDTO {

    // Atributos da Classe -> Características
	
	private Integer id_cliente;
	private String nomecli;
	private String cpfcli;
	private String telefonecli;
	private String totalcli;
	
	public Integer getId_cliente() {
		return id_cliente;
	}
	public void setId_cliente(Integer id_cliente) {
		this.id_cliente = id_cliente;
	}
	public String getNomecli() {
		return nomecli;
	}
	public void setNomecli(String nomecli) {
		this.nomecli = nomecli;
	}
	public String getCpfcli() {
		return cpfcli;
	}
	public void setCpfcli(String cpfcli) {
		this.cpfcli = cpfcli;
	}
	public String getTelefonecli() {
		return telefonecli;
	}
	public void setTelefonecli(String telefonecli) {
		this.telefonecli = telefonecli;
	}
	public String getTotalcli() {
		return totalcli;
	}
	public void setTotalcli(String totalcli) {
		this.totalcli = totalcli;
	}
    
    

	
    
}